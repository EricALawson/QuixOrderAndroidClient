package com.example.quixorder.model;

public class MenuItem {
    String description;
    String image;
    String name;
    Float price;
    String type;

    public MenuItem() {

    }

    public String getDescription() {
        return description;
    }

    public String getImage() {
        return image;
    }

    public String getName() {
        return name;
    }

    public Float getPrice() {
        return price;
    }

    public String getType() {
        return type;
    }
}
